<?php

/*
|--------------------------------------------------------------------------
| Database Configuration
|--------------------------------------------------------------------------
*/

$hostname = "localhost";
$username = "root";
$password = "";
$database = "cosmos_laundry";

/*
|--------------------------------------------------------------------------
| Create Connection
|--------------------------------------------------------------------------
*/

$conn = mysqli_connect(
    $hostname,
    $username,
    $password,
    $database
);

/*
|--------------------------------------------------------------------------
| Check Connection
|--------------------------------------------------------------------------
*/

if (!$conn) {

    die("Database Connection Failed : " . mysqli_connect_error());
} else {
    // echo "connected";
}

/*
|--------------------------------------------------------------------------
| Set UTF-8 Charset
|--------------------------------------------------------------------------
*/

mysqli_set_charset($conn, "utf8mb4");
